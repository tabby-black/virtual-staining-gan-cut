# an ENVI cube is a 3D data structure (hence cube) used to process and visualise images with multiple spectral bands
# a header file eg. .hdr and its corresponding binary file eg. .dat or .img together store one hyperspectral image cube in ENVI format
# .hdr tells ENVI and the loader how to interpret the .dat binary
# each ROI has 3 cubes: raw, white and dark

# helper function to load ENVI hyperspectral cube
def load_hyperspectral_cube(hdr_path):
    """
    Parameters
    ---------- 
    hdr_path : str
        File location of the ENVI header related to the hyperspectral data

    Variables
    ----------
    cube_metadata
        metadata object containing metadata about the hs cube eg. image dimensions

    Returns
    ----------
    hs_data : ndarray (np.float32)
        3D numpy array containing radiance/intensity measurements from the camera sensor. It stores an array of values at consecutive wavelengths (spectrum) for each pixel ie. each row and column. array contains hyperspectral cube

    wavelengths : ndarray
        1D nump array containing spectral bands corresponding to the hyperspectral cube
    """
    
    # parses .hdr file to read useful metadata eg. image dimensions
    # loads binary data from paired .dat file
    # array.shape = (height, width, spectral_channels)
    cube_metadata = spio.envi.open(hdr_path)
    # no pixel data has been loaded at this point - only the framework for it
    # specifically load array with float values for calculations
    # this loads the actual cube
    hs_data = cube_metadata.load().astype(np.float32)
    wavelengths = cube_metadata.bands.centers
    return hs_data, wavelengths, cube_metadata


# helper function for band reduction
# need to import wavelengths from calibration file
def reduce_spectral_dimensions(intensity_array, wavelengths, n=3):
    """
    Parameters
    ----------
    input_data : ndarray
        Array containing the raw hyperspectral cube
    wavelengths : ndarray
        1D nump array containing spectral bands corresponding to the hyperspectral cube
    n : Integer
        width of the spectral window

    Returns
    ----------
    band_reduced_cube : ndarray
        3D numpy array containing the hyperspectral cube after band reduction
    wavelength_reduced : ndarray
        1D numpy array containing the spectral bands corresponding to the hyperspectral cube after the band reduction
    """
    w = np.ones(n)/n
    moving_averaged_image = scipy.ndimage.convolve1d(intensity_array, w, axis=2)
    band_reduced_cube = moving_averaged_image[:,:,1:-1:n]
    wavelength_reduced = wavelengths[1:-1:n]
    return band_reduced_cube, wavelength_reduced

#Â helper function for image registration
def register_hsi_image(rgb_image, hsi_cube):
    # rgb image: H_rgb x W_rgb x 3
    rgb = np.asarray(rgb_image).astype(np.float32)
    # hyperspectral cube: H x W x B (B = 275)
    hsi = np.asarray(hsi_cube).astype(np.float32)

    # create a 2d registration image from HSI using PCA
    H, W, B = hsi.shape
    # reshape(pixels, bands)
    hsi_2d = hsi.reshape(-1, B)
    pca = PCA(n_components=1)
    hsi_pca1 = pca.fit_transform(hsi_2d).reshape(H,W)
    #normalise for stability
    hsi_pca1 = (hsi_pca1 - hsi_pca1.min()) / (hsi_pca1.ptp() + 1e-8)

    # convert rgb image to grayscale
    # red channel + green channel + blue channel
    # weights are different because red, green and blue do not contribute equally to brightness
    rgb_gray = 0.2989 * rgb[..., 0] + 0.5870 * rgb[..., 1] + 0.1140 * rgb[..., 2]
    # normalise to scale between [0, 1] and for stability
    # ptp = peak-to-peak = range of values in rgb image array
    rgb_gray = (rgb_gray - rgb_gray.min()) / (rgb_gray.ptp() + 1e-8)

    # estimate similarity transform
    result = imreg_dft.similarity(rgb_gray, hsi_pca1)

    # build affine transformation matrix using estimated parameters
    theta = np.deg2rad(result['angle'])
    scale = result['scale']
    tx, ty = result['tvec']
    affine = np.array([
        [scale * np.cos(theta), -scale * np.sin(theta), tx],
        [scale * np.sin(theta), scale * np.cos(theta),  ty],
        [0,                     0,                       1]
    ])

    # apply transform to all HSI bands
    H_rgb, W_rgb = rgb_gray.shape
    # registered hsi image must be same number of pixels as rgb image
    # scipy uses inverse mapping
    inv_affine = np.linalg.inv(affine)
    registered_hsi = np.zeros((H_rgb, W_rgb, B), dtype=np.float32)
    # iterate over all bands in hsi image to transform each one
    for b in range(B):
        registered_hsi[..., b] = affine_transform(
            hsi[..., b],
            inv_affine[:2, :2],
            offset=inv_affine[:2, 2],
            output_shape=(H_rgb, W_rgb),
            #bilinear interpolation
            order=1
        )

    # registered_hsi is now aligned to rgb, same spatial shape, and spectrally consistent across bands
    return registered_hsi


